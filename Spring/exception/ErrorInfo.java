package com.cg.exception;

public class ErrorInfo {

	private String massege;

	public ErrorInfo() {
		super();
	}

	public ErrorInfo(String massege) {
		super();
		this.massege = massege;
	}

	public String getMassege() {
		return massege;
	}

	public void setMassege(String massege) {
		this.massege = massege;
	}
	
	
	
}
